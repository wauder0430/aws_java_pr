public class ex05
{
	public static void main(String[] args)
	{
		// 기본연산 + - * / %
		int i = 10;
		int j = 3;
		int result;
		
		result = i + j;
		System.out.println(" i + j = " + result);

		result = i - j;
		System.out.println(" i - j = " + result);

		result = i * j;
		System.out.println(" i * j = " + result);

		result = i / j;
		System.out.println(" i / j = " + result);

		result = i % j;
		System.out.println(" i % j = " + result);
	}
}